func main()
{
    print("The final result is: \(fib(int:n)).")
}
func fib(int:Int) -> Int
{
    print("Calculating fibonacci(\(int))...")
    //If the variable "int" is less than or equal to 2,
    if int <= 2
    {
        print("Terminal case, returning 1.")
        return 1
    }
    //If the variable "int" is greater than 2,
    else
    {
        let number = fib(int:int-1) + fib(int:int-2)
        print("Non-terminal case, returning \(number).")
        return number
    }
}
